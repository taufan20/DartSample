void main(List<String> args) {
  // Data Types

  // Numbers, Strings, Booleans, Lists, Maps, Dynamic

  // Strings
  String firstName = "Taufan";
  print("String: $firstName");

  // Integers
  int age = 34;
  print("Integer: $age");

  // Doubles
  double phi = 3.14;
  print("Double: $phi");

  // Booleans
  bool isPass = true;
  print("Boolean: $isPass");

  // Dynamic
  dynamic random = "Random";
  print("Dynamic: $random");
  random = 140;
  print("Dynamic: $random");
}